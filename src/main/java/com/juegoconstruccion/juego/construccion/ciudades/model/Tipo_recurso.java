package com.juegoconstruccion.juego.construccion.ciudades.model;

/**
 * Enumeración que define los tipos de recursos disponibles en el juego.
 * Los recursos son elementos esenciales que se utilizan en la construcción
 * y el desarrollo de ciudades.
 */
public enum Tipo_recurso {
    /**
     * Representa el recurso de tipo piedra,
     * utilizado para diversas construcciones.
     */
    PIEDRA,

    /**
     * Representa el recurso de tipo oro,
     * requerido para la posiblidad de construccion de un edificio.
     */
    ORO,

    /**
     * Representa el recurso de tipo agua,
     * requerido para la posibilidad de construir un edificio.
     */
    AGUA
}
