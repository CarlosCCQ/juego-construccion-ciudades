package com.juegoconstruccion.juego.construccion.ciudades.model.edificio;

/**
 * Enumeración que representa los tipos de edificio en el juego de construcción de ciudades.
 * Los edificios se clasifican en tres categorías:
 * 
 * - **CLASE_ALTA**: Edificios de clase alta, generalmente más caros y con mayor prestigio.
 * - **CLASE_MEDIA**: Edificios de clase media, que son equilibrados en costo y beneficios.
 * - **CLASE_BAJA**: Edificios de clase baja, más económicos, pero con menores beneficios.
 */
public enum Tipo_edificio {
    CLASE_ALTA,  // Edificios de clase alta
    CLASE_MEDIA, // Edificios de clase media
    CLASE_BAJA   // Edificios de clase baja
}
